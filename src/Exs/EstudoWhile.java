package Exs;

public class EstudoWhile {
    public static void main(String[] args) {
        int i = 0;
        int j = 0;

        while (i < 5) { //while normal
            System.out.println(i); i ++;
            
        }

        //do while
       do {
            System.out.println(j); j++;
        
       } while (j < 6);
       //x
    }
    
}
